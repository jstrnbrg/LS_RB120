class Card
  attr_reader :rank, :suit
  include Comparable

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def value
    case rank
    when "Jack" then 11
    when "Queen" then 12
    when "King" then 13
    when "Ace" then 14
    else rank
    end
  end

  def <=>(other_card)
    value <=> other_card.value
  end

  def to_s
    "#{rank} of #{suit}"
  end
end

class Deck
  attr_reader :deck
  RANKS = ((2..10).to_a + %w(Jack Queen King Ace)).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  def initialize
    @deck = initialize_deck
  end

  def empty?
    deck.size == 0
  end

  def draw
    initialize_deck if @deck.empty?
    @deck.pop
  end

  private
  def initialize_deck
    cards = RANKS.product(SUITS).map do |rank, suit|
      Card.new(rank, suit)
    end
    @deck = cards.shuffle
  end
end

class PokerHand
  attr_reader :hand, :values
  def initialize(deck)
    @hand = initialize_hand(deck)
    @values = initialize_values(hand)
  end

  def print
    puts @hand
  end

  def initialize_hand(deck)
    cards = []
    5.times do
      cards << deck.draw
    end
    cards
  end

  def initialize_values(hand)
    hand.map(&:value)
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def pair?
    values.any? {|val| values.count(val) == 2}
  end

  def two_pair?
    values.select {|val| values.count(val) == 2}.uniq.size == 2
  end

  def three_of_a_kind?
    values.any? {|val| values.count(val) == 3}
  end

  def straight?
    values.sort.each_cons(2).all? {|x,y| y == x + 1 }
  end

  def flush?
    hand.map(&:suit).uniq.size == 1
  end

  def full_house?
    three_of_a_kind? && pair?
  end

  def four_of_a_kind?
    values.any? {|val| values.count(val) == 4}
  end

  def straight_flush?
    flush? && straight?
  end

  def royal_flush?
    straight_flush? && values.any? {|val| val == 14}
  end
end


